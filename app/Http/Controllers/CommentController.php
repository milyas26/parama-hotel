     */
    public function store(Request $request)
    {
        Comment::create([
            'comment' => $request->comment,
            'complain_id' => $request->complain_id,
            'user_id' => auth()->user()->id
        ]);

        $complain   = Complaint::find($request->complain_id);
        $comment    = Comment::where('complain_id', $request->complain_id)->get();

        return view('dashboard.pages.complaint.show', compact('complain', 'comment'));
    }

